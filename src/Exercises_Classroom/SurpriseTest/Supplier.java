package Exercises_Classroom.SurpriseTest;

import java.util.ArrayList;

public class Supplier {

    //Atributtes

    private String name;
    private int age;
    private String city;
    //Constructor

    public Supplier(String name, int age, String city) {
        this.name = name;
        this.age = age;
        this.city = city;
    }

    //Getters

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getCity() {
        return city;
    }

}
